// Arquivo JS sobre Operadores e Operações
let n1 = 10   // número inteiro
let n2 = 2    // número inteiro
let n3 = "2"  // String de texto

document.write(`N1 ${n1} <br>`)
document.write(`N2 ${n2} <br>`)
document.write(`N3 ${n3} <br>`)

// Operadores Aritiméticos
console.log(n1 + n2)  // Adição
console.log(n1 - n2)  // Subtração
console.log( n1 * n2) // Multiplicação
console.log( n1 / n2) // Divisão
console.log(n1 % n2)  // Resto ou módulo

console.log(n2 == n3)      // Igual a
console.log(n2 === 3)     // Idêntico a
console.log(n2 != n3)    // Não é igual (diferente.)
console.log(n2 !== n3)  // Não é idêntico

// Operadores Relacionais
console.log( n1 > n2)
console.log( n1 < n2)
console.log(n1 >= n2)
console.log( n1 <= n2)


// Operadores lógicos
let a = true
let b = false
console.log(a && b)    // E ou AND
console.log( a || b)  // OU OR
console.log(!a)      // NÃO ou NOT
console.log(!b)     

